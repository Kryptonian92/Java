
public class Casting {
	public static void main(String[] args){
		double d = 35.25;
		double dd = 35.99;
		
		int i = (int) d;
		int ii = (int) dd;
		System.out.println(i);
		System.out.println(ii);
		System.out.println(d);
		
		int a = 35;
		float f = a;
		System.out.println("The number is: " + f);
	}
}
